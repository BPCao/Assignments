class Calculator:
    def addition(self, num1, num2):
        result = num1 + num2
        return result

    def subtraction(self, num1, num2):
        result = num1 - num2
        return result

    def multiply(self, num1, num2):
        result = num1 * num2
        return result

    def divide(self, num1, num2):
        result = num1 / num2
        return result
